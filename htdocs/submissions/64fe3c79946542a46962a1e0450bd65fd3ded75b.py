L=[' _ ','| |','|_|','   ','  |','  |',' _ ',' _|','|_ ',' _ ',' _|',' _|','   ','|_|','  |',' _ ','|_ ',' _|',' _ ','|_ ','|_|',' _ ','  |','  |',' _ ','|_|','|_|',' _ ','|_|',' _|']
def seven_seg(x):
    o=''
    for i in range(3):
        for t in x:
            o+=L[int(t)*3+i]
        o+='\n'
    return o